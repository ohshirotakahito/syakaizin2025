#5. モル濃度の計算:
#
#ある溶液のモル濃度が 0.05mol/L であり、その溶液を10ml 使用して，脱イオン水を50mlをくわえて新しい溶液を作成した場合、新しい溶液のモル濃度を計算してください。

# 初期のデータ
C1 = 0.05  # mol/L
V1 = 10  # ml

# 新しい溶液のボリューム
V2 = V1 + 50  # 10mlの初溶液 + 50mlの脱イオン水

# 新しい溶液のモル濃度の計算
C2 = (C1 * V1) / V2

print(C2)


